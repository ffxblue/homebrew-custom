class CliBluestrap < Formula
  desc 'cli-bluestrap'
  homepage 'https://bitbucket.org/ffxblue/cli-bluestrap'
  url 'https://s3-ap-southeast-2.amazonaws.com/infrastructure-clibluestrap-development-v1/cli-bluestrap-0.20.0.tar.gz'
  sha256 '12f50e8820eb99cbad9960d22f1306ccc879e397e089434b1f9c3029554f4db4'
  version '0.20.0'

  conflicts_with "pharod", :because => "it is broken"

  def install
    ENV.prepend_path "PATH", "/usr/local/bin"
    ENV["PREFIX"] = prefix
    system 'make', 'install', 'VERSION=0.20.0'
  end

  test do
    system 'bs', '--help'
  end
end
